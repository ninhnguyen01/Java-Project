public class loopExample {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello".charAt(i));
        }

        int[] num = {1, 2, 3, 4, 5};

        for (int i = 0; i < num.length; i++){
            System.out.println(num[i]);
        }
    }
}
